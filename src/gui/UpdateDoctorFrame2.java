    private boolean validateInput() {
        this.selEmpName=this.txtRecName.getText().trim();
        this.prvLoginId=this.txtPrvLoginId.getText().trim();
        this.loginId=this.txtLoginId.getText().trim();
        char []pwd1=this.txtPassword.getPassword();
        char []pwd2=this.txtRePassword.getPassword();
        if(loginId.isEmpty()==true || pwd1.length==0 || pwd2.length==0)
            return false;
        password=new String(pwd1).trim();
        rePassword=new String(pwd2).trim();
        return true;
    }

    private String matchPassword() {
        if(password.length()<3 || rePassword.length()<3)
            return "Password must be of atleast 3 characters in length!";
        if(password.equals(rePassword)==false)
            return "Password do not match!";
        return null;
    }

