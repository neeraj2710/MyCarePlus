    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        boolean isInputValid=validateInput();
        if(isInputValid==false){
            JOptionPane.showMessageDialog(null,"Please input all fields!");
            return;
        }
        String result=matchPassword();
        if(result!=null){
            JOptionPane.showMessageDialog(null, result);
            return;
        }
        try{
            UserPojo user=new UserPojo();
            user.setLoginId(loginId);
            String encPassword=new String(PasswordEncryption.getEncryptedPassword(password));
            user.setPassword(encPassword);
            user.setUserType("DOCTOR");
            user.setUserName(selEmpName);
            DoctorPojo rec=new DoctorPojo();
            rec.setDoctorId(jcRecId.getSelectedItem().toString());
            rec.setGender(jcGender.getSelectedItem().toString());
            rec.setDoctorName(selEmpName);
            rec.setEmailId(txtEmailId.getText().trim());
            rec.setContactNo(txtContactNo.getText().trim());
            boolean result1=UserDao.updateUser(prvLoginId,user);
            boolean result2=DoctorsDao.updateDoctor(rec);
            if(result1==true && result2==true){
                JOptionPane.showMessageDialog(null,"Doctor udated successfully!");

                return;
            }
            JOptionPane.showMessageDialog(null,"Doctor updation unsuccessfull!");
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Error in DB:"+ex.getMessage());
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

